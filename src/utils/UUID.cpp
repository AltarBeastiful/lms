#include "UUID.hpp"

#include <regex>

#include "Utils.hpp"

static
bool
stringIsUUID(const std::string& str)
{
	static const std::regex re { R"([0-9a-fA-F]{8}\-[0-9a-fA-F]{4}\-[0-9a-fA-F]{4}\-[0-9a-fA-F]{4}\-[0-9a-fA-F]{12})"};

	return std::regex_match(str, re);
}


template<>
std::optional<UUID>
readAs(const std::string& str)
{
	if (!stringIsUUID(str))
		return std::nullopt;

	return UUID {str};
}

